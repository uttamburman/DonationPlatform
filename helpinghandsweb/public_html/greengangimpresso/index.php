<section data-role="section" id="howtoplanttree">
	<div class="container cn2">
    	<div class="joinustext">How to do?</div>
        <ul>
        	<li>Dig a hole considerably large so that the roots of the plant can be comfortably put into.</li>

			<li>Clean the weeds from at least a meter diameter area around the plant so that it gets proper nutrition and sunshine. </li>

 			<li>Keep 5 ( meter distance between adjacent plants so that they have enough space to grow together. </li>

 			<li>Do not plant tree too close to concrete walls. Keep half a meter distance at least. </li>

			<li>Do not use any fertilizer before planting as fertilizer also encourages weeds to grow. </li>

 			<li>Make sure the soil is a little moist. If the soil is not enough moist pour water in the dug area and plant the tree after an hour on watering. </li>

			<li>Make sure that there is not much water accumulated in the soil as it can restrict the plant’s growth. </li>
                </ul>
    </div>
